export const RegExp = {
    Email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
    URL: /^(https?|ftp):\/\/[^\s/$.?#].[^\s]*$/,
    UUID: /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/,
    Date: /^\d{4}-\d{2}-\d{2}$/,
    Time: /^([01]\d|2[0-3]):[0-5]\d(:[0-5]\d)?(\.\d+)?$/,
    DateTime: /^\d{4}-\d{2}-\d{2}T([01]\d|2[0-3]):[0-5]\d(:[0-5]\d)?(\.\d+)?$/,
    Phone: /^\+?[1-9]\d{1,14}$/,
    PostalCode: /^\d{5}(-\d{4})?$/
};
