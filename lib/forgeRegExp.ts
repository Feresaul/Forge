export const RegExp = {
    Email: /^[^\s@]+@[^\s@]+\.[^\s@]+$/,
    StrongPassword:
        /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/,
    URL: /^(https?|ftp):\/\/[^\s/$.?#].[^\s]*$/,
    UUID: /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/,
    Date: /^\d{4}-\d{2}-\d{2}$/,
    Time: /^([01]\d|2[0-3]):[0-5]\d(:[0-5]\d)?(\.\d+)?$/,
    DateTime: /^\d{4}-\d{2}-\d{2}T([01]\d|2[0-3]):[0-5]\d(:[0-5]\d)?(\.\d+)?$/,
    Phone: /^\+?[1-9]\d{1,14}$/,
    PostalCode: /^\d{5}(-\d{4})?$/,
    HexColor: /^#?([a-f0-9]{6}|[a-f0-9]{3})$/i,
    IP: /^(25[0-5]|2[0-4]\d|[01]?\d{1,2})(\.(25[0-5]|2[0-4]\d|[01]?\d{1,2})){3}$/,
    CreditCard:
        /^(?:4[0-9]{12}(?:[0-9]{3})?|5[1-5][0-9]{14}|6(?:011|5[0-9]{2})[0-9]{12}|3[47][0-9]{13}|3(?:0[0-5]|[68][0-9])[0-9]{11}|(?:2131|1800|35\d{3})\d{11})$/,
    Alphanumeric: /^[a-zA-Z0-9]+$/,
    Alpha: /^[a-zA-Z]+$/,
    Numeric: /^\d+$/,
    Lowercase: /^[a-z]+$/,
    Uppercase: /^[A-Z]+$/,
    NotEmpty: /^(?!\s*$).+/
};
